class ForEO
  {
    public static void main(String args[])
    {
      int i;
      System.out.println("even num are ");
      for(i=1; i<=10; i++)
        {
          if(i%2==0)
          System.out.println(i);
      }
      System.out.println("odd num are ");
      for(i=1;i<=10;i++)
        {
          if(i%2!=0)
            System.out.println(i);
        }
    }
  }